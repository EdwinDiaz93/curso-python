

s='Soy fan de los videojuegos'

# para acceder a los caracteres de una cadena de texto se hace con corchetes []
# los indicies de las posiciones inician en cero

# imprime en consola 'S' primera posicion
print(s[0]) 

# imprime en consola 'a'

print(s[5])

# se pueden utilizar indices negativos para obtener los caracteres del final al inicio

print(s[-7])

# Para extraer una porcion de string se utiliza s[ n:m ]
# donde n es la posicion inicio y m la posicion fin
# cuando se obtiene los caracteres m no se toma en cuenta

print(len(s))


print(s[15:])

# para obtener todo el string de nuevo no indicar numeros

print(s[:])


